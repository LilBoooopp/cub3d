#include "cub3d.h"

const char	*g_digit[10] = {
	"01110100011000110001100011000101110",
	"00100011000010000100001000010001110",
	"01110100010000101110100001000011111",
	"01110100010000101110000011000101110",
	"00010001100101011111000100001000010",
	"11111100001111000001000011000101110",
	"01110100011000011110100011000101110",
	"11111000010001000100010000100001000",
	"01110100011000101110100011000101110",
	"01110100011000101111000011000101110"
};

void	draw_px(t_cub *c, int x, int y)
{
	int	col;
	int	i;
	int	j;

	col = 0x000000;
	i = -1;
	while (++i < 2){
		j = -1;
		while (++j < 2)
		{
			mlx_pixel_put(c->mlx, c->mlx_win, x + j, y + i, col);
		}
	}
}

void	draw_digit(t_cub *c, char d, t_vec2i oi, int s)
{
	int	x;
	int	y;
	int	idx;

	if (d < '0' || d > '9')
		return ;
	y = 0;
	while (y < 7)
	{
		x = 0;
		while (x < 5)
		{
			idx = y * 5 + x;
			if (g_digit[d - '0'][idx] == '1')
				draw_px(c, oi.x + x * s, oi.y + y * s);
			x++;
		}
		y++;
	}
}
